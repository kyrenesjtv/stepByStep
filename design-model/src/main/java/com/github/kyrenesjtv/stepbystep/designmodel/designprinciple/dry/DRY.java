package com.github.kyrenesjtv.stepbystep.designmodel.designprinciple.dry;

/**
 * @author huojianxiong
 * @Description DRY - Don’t Repeat Yourself。 不要重复造轮子
 * @Date 2022/2/27 21:34
 */
public class DRY {

    /*
    代码的重复有3种情况
    1：实现逻辑重复
        功能不同，实现逻辑相同。不重复
    2：功能语义重复
        函数命名不同，实现逻辑不同，但是功能是相同的。重复
    3：代码执行重复
        代码在顺序执行的时候，同一方法被通次执行。重复
     */
}
